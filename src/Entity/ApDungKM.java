package Entity;

public class ApDungKM {

    private int idKM, idSP;

    public ApDungKM() {
    }

    public ApDungKM(int idKM, int idSP) {
        this.idKM = idKM;
        this.idSP = idSP;
    }

    public int getIdKM() {
        return idKM;
    }

    public void setIdKM(int idKM) {
        this.idKM = idKM;
    }

    public int getIdSP() {
        return idSP;
    }

    public void setIdSP(int idSP) {
        this.idSP = idSP;
    }

}
